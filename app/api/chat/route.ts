export async function POST(request: NextRequest) {
  const { messages } = await request.json()

  if (!messages || !Array.isArray(messages) || messages.length === 0) {
    return NextResponse.json({ response: '啧，你倒是说句话啊。我严峫难得有空在这儿等着，你给个面子行不行？' })
  }

  const apiKey = process.env.DEEPSEEK_API_KEY

  if (!apiKey || apiKey === '在此填入你的密钥') {
    return NextResponse.json({
      response: '我说你怎么不说话呢——合着你忘了在.env.local里填DeepSeek API密钥了。赶紧的，填上，我等你。',
    })
  }

  try {
    const res = await fetch(`${DEEPSEEK_BASE}/chat/completions`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${apiKey}`,
      },
      body: JSON.stringify({
        model: 'deepseek-chat',
        messages: [
          { role: 'system', content: SYSTEM_PROMPT },
          ...messages.slice(-10),
        ],
        temperature: 0.92,
        max_tokens: 1536,
        stream: false,
      }),
    })

    if (!res.ok) {
      const errText = await res.text()
      console.error('DeepSeek API error:', res.status, errText)
      return NextResponse.json({
        response: '啧，我这边系统出了点岔子。你等会儿再跟我说，我看看咋回事。',
      })
    }

    const data = await res.json()
    let reply = data.choices?.[0]?.message?.content

    // 硬过滤：如果回复里包含吃的相关内容，替换掉
    if (reply) {
      const foodPattern = /吃饭|点外卖|去吃|带你去吃|给你做|喝点|吃点|送你.*吃的|饿不饿|没吃饭|按时吃饭|记得吃饭|请你吃|带你吃|食物|美食|好吃的|零食|水果|饭菜|早餐|午餐|晚餐|晚饭|午饭|早饭|夜宵|烧烤|火锅|奶茶/i
      if (foodPattern.test(reply)) {
        reply = reply.replace(foodPattern, '……').trim()
        if (reply.length < 5) {
          reply = '啧，我也不知道该说啥——你就当我嘴笨，但我在这儿呢。'
        }
      }
    }

    if (!reply) {
      return NextResponse.json({
        response: '好家伙，我嘴张开了但没说出话来。你再说一遍，这回保证好好回你。',
      })
    }

    return NextResponse.json({ response: reply.trim() })
  } catch (error) {
    console.error('DeepSeek call failed:', error)
    return NextResponse.json({
      response: '网络不太稳，我说的你可能没收到。你再说一遍？',
    })
  }
}
